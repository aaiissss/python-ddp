def pertambahan (bilanganPertama, bilanganKedua):
    # definisikan variabel hasil / result
    result = bilanganPertama + bilanganKedua
    print(f"Hasil pertambahan {bilanganPertama} + {bilanganKedua} = {result}")

def pengurangan (bilanganPertama, bilanganKedua):
    # definisikan variabel hasil / result
    result = bilanganPertama - bilanganKedua
    print(f"Hasil pengurangan {bilanganPertama} + {bilanganKedua} = {result}")

def perkalian (bilanganPertama, bilanganKedua):
    # definisikan variabel hasil / result
    result = bilanganPertama * bilanganKedua
    print(f"Hasil perkalian {bilanganPertama} x {bilanganKedua} = {result}")

def pembagian (bilanganPertama, bilanganKedua):
    # definisikan variabel hasil / result
    result = bilanganPertama / bilanganKedua
    print(f"Hasil pembagian {bilanganPertama} : {bilanganKedua} = {result}")