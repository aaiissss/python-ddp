def tambah (angka1, angka2) :
    hasil = angka1+angka2
    print('Hasil dari', angka1, '+', angka2, '=', hasil)

def kurang (angka1, angka2) :
    hasil = angka1 - angka2
    print('Hasil dari', angka1, '-', angka2, '=', hasil)

def kali (angka1, angka2):
    hasil = angka1 * angka2
    print('Hasil dari', angka1, '*`', angka2, '=', hasil)