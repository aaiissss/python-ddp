'''
    * Dictionary
    * - Tipe data dictionary adalah kumpulan pasangan key : value
    * - Tipe data dictionary di tulis dengan {}
    * - Tipe data dictionary di pisah dengan tanda (,)
    * - Tipe data dictionary key nya harus unik
    * - Tipe data dictionary value nya bisa di ubah
    * - Tipe data dictionary key nya tidak bisa di ubah
'''

# Data dictionary person
person = {
	"name": "Budi",
	"age": 20,

	"adress": "Karawang, Jawa Barat"
}

# Ubah nialai / value dalam variabel person : dictionary
person["name"] = "Aditya"

# Menampilkan / print semua data di dictionary person
print (person)